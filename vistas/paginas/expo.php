        <p class="">
            El Gobierno de Reconciliación y Unidad Nacional a través del Ministerio de Economía Familiar realizan la IV edición de EXPOPYME Nicaragua, bajo el lema “Modernizando a las PYMES para la Economía Global” con el propósito de promover el desarrollo y operatividad tecnológica de las Pymes.
            Del 03 al 05 de septiembre los empresarios a nivel nacional tendrán la oportunidad de fortalecer sus conocimientos sobre la modernización a través de espacios de asesorías técnicas que permitan pasar de la supervivencia a la rentabilidad.
            Este evento contará con la participación de más de 100 PYMES de todos los sectores económicos y se realizará bajo una dinámica integral e interactiva que permitirá el desarrollo de negociaciones directas, a través de indicaciones por parte de especialistas nacionales y extranjeros.
