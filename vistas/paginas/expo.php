            <li><span>Matchmaking multisectorial:</span> Espacio enfocado a la competitividad y oportunidades de negocios. Puntos de encuentros para negociaciones entre Pymes industrializadas, inversionistas y socios tecnológicos (Plataformas web, e-commerce, entre otras) en articulación con MIFIC, CETREX, CONICYT, CONIMIPYME y CENPROMYPE.</li>
            <li><span>Innovando con Design Thinking:</span> Espacio donde los empresarios podrán rediseñar o crear una nueva propuesta de valor, a través de la metodología del “Pensamiento del Diseño” con especialistas del Consejo Nacional de Universidades (CNU).</li>
            <li><span>Ruta Emprendedora:</span> Se habilitarán Stands informativos (MIFIC - VUI, INIFOM - Alcaldía de Managua, CETREX, INTA, IPSA, INATEC- CI, TELCOR – CEABAD, INTUR, Correos de Nicaragua y MINSA), facilitando asesorías permanentes con información sobre las diferentes actividades que se realizan para el impulso de las Pymes.</li>
            <li><span>Cine Technoclub:</span> Se contará con un espacio de cine para proyectar películas con temas que fomenten la innovación y tecnologías.</li>
            <li><span>Expo feria de Productos y Servicios:</span> Se habilitarán espacios a las Pymes destinados para la exposición y comercialización de las distintas ofertas de productos y servicios los que estarán adecuados y visibilizados de acuerdo al sector productivo.</li>
            <li><span>Expo Gastronómica:</span> Un espacio que muestra la riqueza gastronómica popular nicaragüense con la transición en comida Gourmet.</li>
            <li><span>Expresiones artísticas:</span> Espacio donde los visitantes podrán disfrutar de presentaciones artísticas de danza y música. </li>
